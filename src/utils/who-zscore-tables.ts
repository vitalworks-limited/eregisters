/**
 * WHO Growth Standards - LMS Reference Tables
 *
 * Lambda (L): Power in Box-Cox transformation
 * Mu (M): Median value
 * Sigma (S): Coefficient of variation
 *
 * Z-score formula: Z = ((value/M)^L - 1) / (L * S)
 *
 * Sources:
 * - WHO Child Growth Standards (0-60 months): https://www.who.int/tools/child-growth-standards
 * - WHO Growth Reference (5-19 years): https://www.who.int/tools/growth-reference-data-for-5to19-years
 */

export interface LMSPoint {
    key: number; // Age in months or height in cm
    L: number;
    M: number;
    S: number;
}

export interface LMSTable {
    boys: LMSPoint[];
    girls: LMSPoint[];
}

